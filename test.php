        <h2>The History Behind The Calculator</h2>
        <h3>Born from a 200 Series Build</h3>
        <p>This calculator wasn’t built by a software company—it was built in a garage.</p>
        <p>It started when I was setting up my own <strong>Toyota LandCruiser 200 Series</strong> for touring. Like many 4WD owners, I had a long wish list: bullbar, winch, drawers, roof rack, dual batteries, and a fridge. But as I started adding up the numbers, I hit a wall.</p>
        <p>I realized that despite the LandCruiser's reputation as the ultimate tourer, the factory payload is surprisingly low once you start modifying it. As a Mechanical Engineer, I built a complex spreadsheet to track every kilogram, calculating moment arms and axle loads to figure out where I sat legally.</p>
    </div>

    <div class="seo-content">
        <h3>The Reality of Modern 4WDs</h3>
        <p>There is a misconception that 4WDs are built ready to cross the continent straight off the showroom floor. The reality is that manufacturers design these vehicles for stock standard conditions. They are not engineered by the factory to carry 400kg of steel protection, recovery gear, and camping equipment while towing a caravan.</p>
        
        <p>Most 4WDs will hit their <strong>Rear Axle Limit</strong> long before they hit their total GVM limit. This creates a dangerous "cantilever effect" where weight in the back lifts the front wheels, reducing steering traction and braking efficiency.</p>
        
        
    </div>

    <div class="seo-content">
        <h3>The "Engineer's Frustration"</h3>
        <p>As a Mechanical Engineer, I found it baffling that a tool like this didn't already exist. We spend tens of thousands of dollars modifying our vehicles, yet figuring out if we are actually legal involves guessing, complex maths, or a trip to a weighbridge when it’s often too late.</p>
        <p>I realized that if I was struggling to balance the numbers with an engineering background, most other 4WDers were likely driving unknowingly overloaded vehicles. I decided to turn my personal spreadsheet into a public resource.</p>
    </div>

    <div class="seo-content">
        <h3>More Than Just Compliance</h3>
        <p>I was shocked when I took my vehicle over a weighbridge with only a bullbar and winch and discovered it was already over its front axle rating. While avoiding fines is critical, this project is about building better, safer vehicles.</p>
        
        <p>A properly balanced vehicle offers:</p>
        <ul>
            <li><strong>Stability:</strong> It handles predictably in emergency manoeuvres.</li>
            <li><strong>Comfort:</strong> The suspension works as intended rather than sitting on the bump stops.</li>
            <li><strong>Capability:</strong> You maintain traction and clearance when off-road.</li>
        </ul>
